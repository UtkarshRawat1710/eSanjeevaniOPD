    private void loadReceptionistsId() {
    try{
            List<String>RecList=ReceptionistDAO.getAllReceptionistId();
            for(String RecId:RecList){
                jcReceptionistId.addItem(RecId);
            }
            if(jcReceptionistId.getItemCount()!=0)
                BtnDelete.setEnabled(true);
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"Error in DB:"+ex);
            ex.printStackTrace();
        }
    }
