    private void BtnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnDeleteActionPerformed
     String RecId=jcReceptionistId.getSelectedItem().toString();
        try{
            boolean result=ReceptionistDAO.deleteReceptionistById(RecId);
            if(result){
                JOptionPane.showMessageDialog(null, "Receptionist removed successfully!");
               jcReceptionistId.removeItem(RecId);
                if(jcReceptionistId.getItemCount()==0){
                    BtnDelete.setEnabled(false);
                    JOptionPane.showMessageDialog(null, "All Receptionists removed!");
                
            }
        }else{
              JOptionPane.showMessageDialog(null, "Deletion unsuccessful");  
            }
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"Error in DB:"+ex);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_BtnDeleteActionPerformed

