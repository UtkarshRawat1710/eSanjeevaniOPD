 private void loadReceptionistId() {
       try{
            List<String> recepIdList=ReceptionistDAO.getAllReceptionistId();
            if(recepIdList==null){
                JOptionPane.showMessageDialog(null,"No Reception Present");
                return;
            }
            for(String id:recepIdList){
             jcReceptionistId.addItem(id);
        }
       }
       catch(SQLException e){
           JOptionPane.showMessageDialog(null,"Error in DB!","ERRORinUpdateRecep", JOptionPane.ERROR_MESSAGE);
           e.printStackTrace();
       } 

    }
    private boolean validateInput(){
      oldPwd=PasswordEncryption.getEncryptedPassword(new String(txtPwd.getPassword()));
      retypePwd=PasswordEncryption.getEncryptedPassword(new String(txtRetypePwd.getPassword()));
      recepName=txtReceptionistName.getText();
      if(txtLoginId.getText().isEmpty()||oldPwd.isEmpty()||retypePwd.isEmpty()){
       return false;  
      }
     if(oldPwd.length()<4||retypePwd.length()<4){
           JOptionPane.showMessageDialog(null,"Password length should be more than 3 characters!","ERROR",JOptionPane.ERROR_MESSAGE);
           txtPwd.setText("");
           txtRetypePwd.setText("");
           return false;
     }
     if(!oldPwd.equals(user.getPassword())){
         JOptionPane.showMessageDialog(null,"Password does not match!","ERROR",JOptionPane.ERROR_MESSAGE);
          txtPwd.setText("");
          txtRetypePwd.setText("");
          return false;
     }
     return true;
    }
